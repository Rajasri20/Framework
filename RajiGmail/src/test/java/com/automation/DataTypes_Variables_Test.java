package com.automation;

public class DataTypes_Variables_Test {

	public static void main(String[] args) {


		int id; // 'id' is a 'Variable', 'int' data type of value is assigned. 'id' is a 'Variable', called Declaration.
		
		id=1111; // '1111' is called 'Assignment' or 'Initialization'
		
		double salary = 99999.99; // 'salary' is a 'Variable'
		
		char gender = 'M'; // 'gender' is a 'Variable'
		
		String name= "Srikrishna"; // 'name' is a 'Variable'
		
		System.out.println(id);
		
		System.out.println(salary);
		
		System.out.println(gender);	
		
		System.out.println(name);
		
		System.out.println(false);
		
		System.out.println(id + " " + salary + " " + gender + " " + name);
		
		// If you want to display all the outputs at the same time use a 'Concatination(+)symbol 
		// and 'Double quotes(" ") with a space in between the Quotes' again a 'Concatination(+)symbol  
	}

}
